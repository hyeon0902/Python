# len() : 리스트의 길이

score = [100, 80, 95, 90, 70]
print(len(score))


# sum() : 리스트 요소의 합

score = [100, 80, 95, 90, 70]
sum(score)
print(sum(score)/len(score))

# max() : 리스트 요소 중 최대값 반환

score = [100, 80, 95, 90, 70]
print(max(score))

# min() : 리스트 요소 중 최소값 반환
score = [100, 80, 95, 90, 70]
print(min(score))